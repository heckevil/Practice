package com.house.people;

public class PeopleVO {
	private String nm;
	private int house;
	private int idep;
	private int iuer;
	private String hnm;
	private String dpname;

	public String getHnm() {
		return hnm;
	}
	public void setHnm(String hnm) {
		this.hnm = hnm;
	}
	public String getDpname() {
		return dpname;
	}
	public void setDpname(String dpname) {
		this.dpname = dpname;
	}
	public int getIuer() {
		return iuer;
	}
	public void setIuer(int iuer) {
		this.iuer = iuer;
	}
	public String getNm() {
		return nm;
	}
	public void setNm(String nm) {
		this.nm = nm;
	}
	public int getIdep() {
		return idep;
	}
	public void setIdep(int idep) {
		this.idep = idep;
	}
	public int getHouse() {
		return house;
	}
	public void setHouse(int house) {
		this.house = house;
	}
	
}

